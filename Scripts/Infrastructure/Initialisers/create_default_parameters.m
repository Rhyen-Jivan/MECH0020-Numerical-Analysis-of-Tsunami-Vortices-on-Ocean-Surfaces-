function profiles = create_collector_machine_profiles()
profiles = struct( ...
    'laptop', struct( ...
        'aliases', {{'ASUS_ZENBOOK', 'ZENBOOK', 'LAPTOP', 'NOTEBOOK'}}, ...
        'panel_profile', 'laptop'), ...
    'desktop', struct( ...
        'aliases', {{'DESKTOP', 'MAIN_PC', 'MAIN_COMPUTER'}}, ...
        'panel_profile', 'desktop'));
end

function machine_profile = resolve_collector_machine_profile(machine_tag, profiles)
machine_profile = 'desktop';
machine_token = lower(strtrim(char(string(machine_tag))));
if isempty(machine_token)
    return;
end

profile_names = fieldnames(profiles);
for i = 1:numel(profile_names)
    profile_name = profile_names{i};
    aliases = profiles.(profile_name).aliases;
    alias_tokens = lower(string(aliases));
    if any(strcmp(machine_token, alias_tokens)) || contains(machine_token, lower(profile_name))
        machine_profile = profile_name;
        return;
    end
end

if contains(machine_token, 'zenbook') || contains(machine_token, 'laptop') || contains(machine_token, 'notebook')
    machine_profile = 'laptop';
end
end

function panel_profile = resolve_collector_panel_profile(machine_profile)
switch lower(strtrim(char(string(machine_profile))))
    case 'laptop'
        panel_profile = 'laptop';
    otherwise
        panel_profile = 'desktop';
end
end

function debug_ic_catalog(catalog, scenarios)
% Lightweight inspector for IC catalog and scenario structs.
% Call with `debug_ic_catalog(params.ic_ui_catalog, params.ic_scenario_catalog)`
%//TODO: This is a developer helper — wrap with a verbosity flag or remove before release.
if nargin < 1 || isempty(catalog)
    disp('debug_ic_catalog: catalog is empty or missing');
    return;
end
fprintf('debug_ic_catalog: %d catalog entries\n', numel(catalog));
for k = 1:numel(catalog)
    e = catalog(k);
    nfields = 0;
    try
        nfields = numel(e.dynamic_fields);
    catch
        nfields = -1;
    end
    fprintf('  %d: %s (%s) — dynamic_fields=%d\n', k, e.display_name, e.ic_type, nfields);
end
if nargin >= 2 && ~isempty(scenarios)
    fprintf('debug_ic_catalog: %d scenarios\n', numel(scenarios));
    for k = 1:numel(scenarios)
        s = scenarios(k);
        locked = double(s.lock_ic_type_dropdown);
        fprintf('  %d: %s -> forced=%s locked=%d\n', k, s.scenario_name, s.forced_ic_type, locked);
    end
end
end
