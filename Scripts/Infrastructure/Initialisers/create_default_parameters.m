function value = pick_struct_numeric(s, field_names, fallback)
value = fallback;
if ~isstruct(s)
    return;
end
for i = 1:numel(field_names)
    key = field_names{i};
    if isfield(s, key) && isnumeric(s.(key)) && isscalar(s.(key)) && isfinite(s.(key))
        value = double(s.(key));
        return;
    end
end
end

